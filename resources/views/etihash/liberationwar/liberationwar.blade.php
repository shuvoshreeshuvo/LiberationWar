    <h1 style="text-align: center;color: red;">মুক্তিযুদ্ধ</h1>
 <p style="text-align:left">বাংলাদেশের স্বাধীনতা যুদ্ধ ছিল ১৯৭১ সালে সংঘটিত তৎকালীন পশ্চিম পাকিস্তানের বিরুদ্ধে পূর্ব পাকিস্তানের সশস্ত্র সংগ্রাম, যার মাধ্যমে বাংলাদেশ একটি স্বাধীন দেশ হিসাবে পৃথিবীর মানচিত্রে আত্মপ্রকাশ করে। ১৯৭১ সালের ২৫ মার্চ রাতের অন্ধকারে পাকিস্তানি সামরিক বাহিনী পূর্ব পাকিস্তানে বাঙালি নিধনে ঝাঁপিয়ে পড়লে একটি জনযুদ্ধের আদলে গেরিলাযুদ্ধ তথা স্বাধীনতা যুদ্ধের সূচনা ঘটে। ২৫ মার্চের কালো রাতে পাকিস্তানি সামরিক বাহিনী ঢাকায় অজস্র সাধারণ নাগরিক, ছাত্র, শিক্ষক, বুদ্ধিজীবী, পুলিশ ও ই.পি.আর.-কে হত্যা করে এবং ১৯৭০ সালের সাধারণ নির্বাচনে নিরঙ্কুশ সংখ্যাগরিষ্ঠতাপ্রাপ্ত দল আওয়ামী লীগ প্রধান বাঙালিদের তৎকালীন জনপ্রিয় নেতা বঙ্গবন্ধু শেখ মুজিবুর রহমানকে গ্রেপ্তার করে। পার্বত্য চট্টগ্ৰামের কালুরঘাট বেতাৱ কেন্দ্র থেকে ৮ম পূৰ্ব বেঙ্গল রেজিমেন্টের উপ-প্ৰধান মেজর জিয়াউর রহমান ও চট্টগ্রাম আওয়ামী লীগের নেতা এম. এ. হান্নান বঙ্গবন্ধু শেখ মুজিবুর রহমানের পক্ষে স্বাধীনতা ঘোষণা করেন। প্রতিষ্ঠিত হয় বাঙালি জাতির স্বাধীন রাষ্ট্র বাংলাদেশ ।

পরিকল্পিত গণহত্যার মুখে সারাদেশে শুরু হয়ে যায় প্রতিরোধযুদ্ধ; জীবন বাঁচাতে কয়েক হাজাৱ আওয়ামী লীগেৱ নেতাৱা পার্শ্ববর্তী দেশ ভারতে আশ্রয় গ্রহণ করে। পাকিস্তানি সামরিক বাহিনীর ইস্ট বেঙ্গল রেজিমেন্ট, ইস্ট পাকিস্তান রাইফেলস (ইপিআর), ইস্ট পাকিস্তান পুলিশ, সামরিক বাহিনীর বাঙালি সদস্য এবং সর্বোপরি বাংলাদেশের স্বাধীনতাকামী সাধারণ মানুষ দেশকে পাকিস্তানি সামরিক বাহিনীর কব্জা থেকে স্বাধীন করতে কয়েক মাসের মধ্যে গড়ে তোলে মুক্তিবাহিনী। গেরিলা পদ্ধতিতে যুদ্ধ চালিয়ে গেরিলা বাহিনী সারাদেশে পাকিস্তানি হানাদার বাহিনীকে ব্যতিব্যস্ত করে তোলে। স্বাধীনতা যুদ্ধ চলাকালীন সময়ে বাংলাদেশ ভারতের কাছ থেকে নুন্যতম অর্থনৈতিক, সামরিক ও কূটনৈতিক সাহায্য লাভ করে। ডিসেম্বরের শুরুর দিকে যখন পাকিস্তানি সামরিক বাহিনীর পতন অনিবার্য হয়ে ওঠে, তখন তারা গেরিলা বাহিনীর কাছে পরাজয়ের লজ্জা এড়াবার জন্য এবং স্বাধীনতা যুদ্ধকে আন্তর্জাতিক সংঘর্ষে পরিণত করার উদ্দেশ্যে ৩ ডিসেম্বর ভারতে বিমান হামলার মাধ্যমে ভারতের বিরুদ্ধে যুদ্ধে লিপ্ত হয়।

অত:পর ভারত বাংলাদেশের স্বাধীনতা যুদ্ধে সরাসরিভাবে জড়িয়ে পড়ে। মুক্তিবাহিনী ও ভারতীয় সামরিক বাহিনীর সম্মিলিত আক্রমণের মুখে ইতোমধ্যে পর্যদুস্ত ও হতোদ্যম পাকিস্তানি সামরিক বাহিনী যুদ্ধ বিৱতীৱ সিদ্ধান্ত গ্রহণ করে। ১৬ ডিসেম্বর ঢাকার রেসকোর্স ময়দানে পাকিস্তান ৯৩,০০০ হাজার সৈন্যসহ আক্শ্মীকভাবে যুদ্ধবিরতির পরিবর্তে আত্মসমর্পণের দলীল সই করে। এসময় পাকিস্তানি বাহিনীর পক্ষ থেকে দলীলে সই করেন আমির আবদুল্লাহ খান নিয়াজি। এরই মাধ্যমে নয় মাস ব্যাপী রক্তক্ষয়ী স্বাধীনতা যুদ্ধের অবসান হয়। ভাৱতীয় বাহিনী বাংলাদেশ আৱ প্ৰায় তিনি মাসেৱও অধিক সময় ৱয়ে যায়। অবশেষে অনেক কূটনৈতিক চাপেৱ মুখে ভাৱত বাংলাদেশ ত্যাগ করতে বাধ্য হয়।

